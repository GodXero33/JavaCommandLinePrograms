	public void renderPageHeader (String page, String padding) {
		if (!this.pagesData.containsKey(page)) return;

		final String[] strArr = this.pagesData.get(page);
		System.out.println();

		for (int i = 0; i < strArr.length; i++) {
			System.out.println(padding + strArr[i]);

			if (i == strArr.length - 2) System.out.println();
		}

		System.out.println();
	}
